<h1>About this site</h1>
<p>
  This is a lightweight web app built with performant svelte components and
  scaffolded with the application framework <a href="https://sapper.svelte.dev/">Sapper</a>.
  This gives us routing out of the box, along with SEO, SSR, code-splitting and many other features.
  For the video functionality, this app uses <a href="https://github.com/twilio/twilio-video.js">Twilio Video</a>
  to create unique chat rooms that your friends can join.
</p>
<h1>Why?</h1>
<p>
  These days, video chat web clients are purposefully hamstrung and have limited
  feature sets to steer users towards native clients. Surely there is a better way.
  You might find this useful if your family wants to see your face, or if you just
  need a quiet place to hang with your buds.
</p>